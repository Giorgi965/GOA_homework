def make_upper_case(s):
    return s.upper()


def simple_multiplication(number) :
    if number % 2 == 0:
        return number * 8
    else:
        return number * 9
    

def string_to_number(s):
    return int (s)


def even_or_odd(number):
    if number % 2 == 0:
        return "Even"
    else:
        return "Odd"